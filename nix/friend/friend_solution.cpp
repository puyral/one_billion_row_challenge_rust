static constexpr uint16_t P1 = 10937;
static constexpr uint16_t P2 = 7577;

struct Stats
{
    int16_t min;
    int16_t max;
    int32_t avg;
    uint32_t cnt;
};

struct StatsF
{
    float min;
    float max;
    float avg;
};

static inline uint64_t hash1(const Name &name)
{
    return name[0] + (name[1] & 0xFF);
}

static inline uint16_t hash2(const uint64_t x)
{
    return static_cast<uint16_t>(x % P1);
}

static inline uint16_t hash3(const uint16_t x)
{
    return static_cast<uint16_t>(x % P2);
}

static inline uint16_t hash_name(const Name &name)
{
    uint64_t h1 = hash1(name);
    uint16_t h2 = hash2(h1);
    uint16_t h3 = hash3(h2);
    uint16_t h4 = HASH_TABLE[h3];

    return h4;
}


static inline const uint8_t *parse_temperature(const uint8_t *beg, int16_t &t)
{
    bool f = *beg == '-';
    int16_t sign = f ? -1 : 1;

    beg += f;

    t = *beg - '0';
    ++beg;

    f = *beg == '.';
    beg += f;
    t *= 10;
    t += *beg - '0';
    ++beg;

    f = *beg == '.';
    beg += f;
    t = f * (t * 10 + *beg - '0') + (1 - f) * t;

    t *= sign;
    beg += f;
    ++beg;

    return beg;
}

int main(int argc, char **argv)
{
    uint8_t *data = nullptr;
    size_t data_sz = 0;

    int fd = open("measurements.txt", O_RDONLY);

    struct stat fd_stats;
    fstat(fd, &fd_stats);
    data_sz = fd_stats.st_size;

    data = reinterpret_cast<uint8_t *>(mmap(nullptr, data_sz, PROT_READ, MAP_SHARED, fd, 0));
    madvise(data, data_sz, MADV_WILLNEED | MADV_RANDOM | MADV_HUGEPAGE | MADV_POPULATE_READ);
    close(fd);

    std::array<std::array<Stats, BUCKETS_N>, THREADS_N> stations{};
    std::array<std::array<Name, BUCKETS_N>, THREADS_N> names{};

#pragma omp parallel num_threads(THREADS_N) shared(stations, names)
    {
        size_t th_id = omp_get_thread_num();

        size_t off = (data_sz / THREADS_N) * th_id;
        size_t end = (data_sz / THREADS_N) * (th_id + 1);

        if (th_id == THREADS_N - 1)
            end = data_sz;

        uint32_t max_idx = 0;
        if (th_id && data[off - 1] != '\n')
        {
            while (data[off] != '\n')
                ++off;
            ++off;
        }

        while (off < end)
        {
            Name name{};
            uint8_t *name_data = reinterpret_cast<uint8_t *>(name.data());
            int16_t value;

            size_t j;
            for (j = 0; data[off + j] != ';'; ++j)
                name_data[j] = data[off + j];

            uint16_t idx = hash_name(name);

            off += j + 1;
            off += parse_temperature(data + off, value) - (data + off);

            names[th_id][idx] = name;
            ++stations[th_id][idx].cnt;
            stations[th_id][idx].avg += value;

            if (value < stations[th_id][idx].min)
                stations[th_id][idx].min = value;

            if (value > stations[th_id][idx].max)
                stations[th_id][idx].max = value;
        }
    }

    std::array<std::pair<Name, StatsF>, BUCKETS_N> sorted;

    for (size_t i = 1; i < THREADS_N; ++i)
    {
        for (size_t j = 0; j < BUCKETS_N; ++j)
        {
            if (stations[i][j].cnt)
                names[0][j] = names[i][j];

            stations[0][j].cnt += stations[i][j].cnt;
            stations[0][j].avg += stations[i][j].avg;

            if (stations[i][j].min < stations[0][j].min)
                stations[0][j].min = stations[i][j].min;

            if (stations[i][j].max > stations[0][j].max)
                stations[0][j].max = stations[i][j].max;
        }
    }

    for (size_t i = 0; i < BUCKETS_N; ++i)
    {
        sorted[i] = {
            names[0][i],
            StatsF{
                .min = static_cast<float>(stations[0][i].min) / 10.0f,
                .max = static_cast<float>(stations[0][i].max) / 10.0f,
                .avg = static_cast<float>(stations[0][i].avg) / stations[0][i].cnt / 10.0f}};
    }

    std::print("{{");
    for (size_t i = 0; i < BUCKETS_N - 1; ++i)
    {
        std::print(
            "{}={:.1f}/{:.1f}/{:.1f}, ",
            reinterpret_cast<const char *>(sorted[i].first.data()),
            sorted[i].second.min,
            sorted[i].second.avg,
            sorted[i].second.max);
    }
    std::print(
        "{}={:.1f}/{:.1f}/{:.1f}",
        reinterpret_cast<const char *>(sorted.back().first.data()),
        sorted.back().second.min,
        sorted.back().second.avg,
        sorted.back().second.max);
    std::println("}}");

    munmap(data, data_sz);

    return 0;
}
